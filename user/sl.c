char* substring(const char* c, int start, int len){
	int totalLen = strlen(c) - start;
	if(totalLen < len){
		len = totalLen;
	}
	char* ans = malloc(len+2);
	memcpy((void*)ans, (void*)(c+start), len);
	ans[len] = '\n';
	ans[len+1] = 0;
	return ans;
}


